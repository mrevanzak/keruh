import SwiftUI

// MARK: - Paper Inko Font
extension Font {
  public static func paperInko(size: CGFloat) -> Font {
    return .custom("PaperInko", size: size)
  }
}

// MARK: - Figtree Font
extension Font {
  public static func figtree(size: CGFloat) -> Font {
    return .custom("Figtree", size: size)
  }
}
